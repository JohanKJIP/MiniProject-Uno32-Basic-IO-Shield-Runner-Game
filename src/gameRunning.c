/**
 * Convert binary to hex number.
 * @param binary number
 * @return hex number
 */
int hexConverter(int binary){
    int hex = 0x0;
    int i;
    for (i = 0; i < 4; i++) {
        int tempMod = binary % 2;
        if(tempMod){
            hex += 0xf;
            hex <<= 4;
        } else {
            hex <<= 4;
        }
        binary >>= 1;
    }
    return hex;
}

/**
 * Update game, this is the game loop.
 */
void updateRunning(FPS) {
    accumulator++;
    if(accumulator > (PROCESSOR_SPEED/FPS)) {
        entities_update();
        display_update();
        accumulator = 0;
    }
}
